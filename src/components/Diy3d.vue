				</p>
				<h3>How Can I Try 3D Printing and Make My Ideas Real Things?</h3>
				<p>If you're interested in 3D printing, you have a few options. First, you can head to your local hackerspace and become a member. Hackerspaces.org has a constantly-updated list of hackerspaces around the globe you can join, and many of them have open-house days so you can stop in and see the facilities before you pay to become a member. If a hackerspace near you has a 3D printer, members undoubtedly have access to it. In some cases, you'll have to either bring your own raw materials, or pony up a little more cash to keep the printer stockpiled with thermoplastics. If you're lucky, it's considered part of your membership fee.</p>
				<p>If you don't have a hackerspace near you, check with your local vocational schools, community colleges, or universities. Here in Washington DC, the Martin Luther King, Jr Memorial Library just added a 3D printer that anyone can use as part of its Digital Commons project. Even if such a thing is unheard of where you are, it doesn't have to be. Get involved with your local library or community college, and make the case—you never know, they may spring for it and you may find yourself the local 3D printing expert.
				</p>
				<p>
				Finally, you can always turn to a professional 3D printing company with a line of industrial printers available to take your order. They can usually do crazy things with powdered materials, lasers, etching, and bring your idea to life in any shape or size you need, without the need for polishing, sanding, or a ton of post-processing after the fact. We mentioned Shapeways and Sculpteo above, both companies can take your ideas and bring them to life. It's a little hands-off compared to visiting a hackerspace and actually using a printer yourself, or buying one of your own, but they do have some resources you probably won't have access to regardless of where you turn.</p>
