				<h3>What Is 3D Printing?</h3>
				<p>Simply, 3D printing is a method of creating a solid physical object from a digital 3D model. The reason it's called 3D "printing" is because the assembly process is similar to way you would print ink on paper. Most popular 3D printers are "extrusion printers," (meaning they spray their material out of maneuverable nozzles) where base material—usually thermoplastic filament—are laid down in layers that eventually create the finished product. 3D printing is an "additive" process, which means that the object is created by adding layers from bottom up, with each layer being printed on top of the last one over and over until the job is done.</p>
				<p>
					3D printing has actually been around for a long time, mostly in industrial settings. Like any technology though, the hardware got smaller and cheaper and the software required got easier to use over time. We even have a Lifehacker Night School on 3D modeling. 3D printers—while still expensive—are cheaper than they've ever been, and work with a variety of 3D modeling programs. They've become extremely popular for people who want to make their own custom products and parts, like the DIY and maker communities.
				</p>
				<h3>Are 3D Printers Useful to Ordinary People Yet? What Can I Do With One?</h3>
				<p>
					3D printers are definitely useful for ordinary people, but the question is really whether ordinary people see a use for them yet. They can be imposing appliances, but that's changing. For example, the Zeus "all-in-one" 3D printer and copier, currently past its funding goal on Kickstarter, aims to make copying physical items and printing them so easy anyone can do it, without even having to model the original object first. It's a few years out, but before long 3D printing will be a ubiquitous technology, one everyone has access to. Still, whether it's useful to the average person right now depends on a basic question: Are you a DIY'er, hacker, or maker?
				</p>
				<p>
					If you're the DIY type, there's no question: they're useful. You can get involved now by buying your own printer or using one available to the public (more on that later.) From there, the sky's the limit. You can print your own objects, components for bigger projects, or even print complex, moving objects. For example, try printing a custom mount for your webcam, or print out broken appliance parts to get more mileage from your expensive equipment. You can even print your own smartphone case to your exacting specifications.
				</p>
				<p>
					If you can model it, you can print it. Even if you can't model it, you may be able to find someone else's model online that you can tweak. If you need to copy an item, you can even DIY your own 3D scanner, use it to model what you want to copy, and import it to Blender for printing. Like we said, the possibilities are endless.
				</p>
				<p>
					If you're not a DIY enthusiast, keep an eye out for when 3D printed products make their way to you in other forms. ChocEdge will 3D print whatever you want...in chocolate (assuming, of course, you don't want to buy your own chocolate printer.) TwinKind will even 3D print a tiny action figure—of you! Doctors and hospitals are already experimenting with 3D printed prosthetics, like this exoskeleton that gave a little girl use of her arms again. If any part of her exoskeleton breaks, or she grows out of it, it can be cheaply reprinted or refitted to her. NASA is experimenting with 3D food printers for space travel, and just issued a grant for a 3D pizza printer. Finally, who hasn't heard of the Liberator, the first 3D printed gun, which is now on display at London's V&A Museum.
				</p>
				<p>
					More practically, companies like Shapeways and Sculpteo will take your model or idea, and then 3D print the final object for you so you don't even need to invest in a printer. Both companies even offer marketplaces where you can sell those creations to others, and just browsing their marketplaces and models that other designers have uploaded can inspire you to make your own things. Thingiverse is probably the web's biggest site for 3D printing projects, patterns, and 3D models. If you're looking for something specific, check there first to see if someone's already made it, or at least made something close to it.
				</p>
